function mergeData(){
    let results = new Array();
    let nyArray = nyData.results;
    let censusArray = censusData.response;
    for (let i = 0; i <censusArray.length; i++){
      let censusResult = censusArray[i];
      let censusString = censusResult[0].split(" ");
      let county = censusString[0];
      let population = censusResult[1];
      if (censusString[2] == "New" && censusString[3] == "York") {
        for (let x = 0; x < nyArray.length; x++){
          let nyResult = nyArray[x];
          if (nyResult.county == county) {
            results.push({
              county_name:nyResult.county,
              county_crime:nyResult.total,
              county_population:population
            })
          }
        }
      }
    }
    return results;
}
  
